function recente() {
    let recentes = document.getElementById("recentes")
    
    let cantores = document.getElementById("cantores")
    
    recentes.style.display = "block"
    cantores.style.display = "none"

}

function cantores() {
    let cantores = document.getElementById("cantores")
    
    let recentes = document.getElementById("recentes")
    
    cantores.style.display = "block"
    recentes.style.display = "none"


}