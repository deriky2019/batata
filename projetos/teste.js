function curtir() {
    let norma = document.getElementById("normal")
    let curtido = document.getElementById("curtido")
    curtido.style.display = "block"
    norma.style.display = "none"

}
function descurtir() {
    let norma = document.getElementById("normal")
    let curtido = document.getElementById("curtido")
    curtido.style.display = "none"
    norma.style.display = "block"



}


