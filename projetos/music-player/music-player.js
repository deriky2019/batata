function pausar() {
    let play  = document.getElementById("play")
    let pause = document.getElementById("pause")
    pause.style.display = "block"
    play.style.display = "none"

}

function despausar() {
    let play  = document.getElementById("play")
    let pause = document.getElementById("pause")
    pause.style.display = "none"
    play.style.display = "block"
}